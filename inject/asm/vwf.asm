	mov r1, r8
	ldrh r0, [r3]
	push {r2, r3, r4, r5}
	ldrh r2, [r7, 0x6] ; src mask
	ldrh r3, [r7, 0x8] ; dst mask
	ldrb r4, [r7, 0x3] ; lsl amt
	ldrb r5, [r7, 0x5] ; lsr amt
	
	lsl r1, r4
	lsr r1, r5
	
	and r0, r2          
